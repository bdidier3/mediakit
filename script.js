function getValueByPath(obj, path) {
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : null), obj);
}

const languageMeta = {
  fr: { label: 'FR', flag: '\uD83C\uDDEB\uD83C\uDDF7' },
  en: { label: 'EN', flag: '\uD83C\uDDEC\uD83C\uDDE7' },
  de: { label: 'DE', flag: '\uD83C\uDDE9\uD83C\uDDEA' },
  nl: { label: 'NL', flag: '\uD83C\uDDF3\uD83C\uDDF1' }
};

function updateLanguageUi(selected) {
  const flag = document.getElementById('lang-flag');
  const label = document.getElementById('lang-label');
  const meta = languageMeta[selected] || languageMeta.en;

  if (flag) {
    flag.textContent = meta.flag;
  }

  if (label) {
    label.textContent = meta.label;
  }

  document.querySelectorAll('.lang-option').forEach((option) => {
    option.classList.toggle('active', option.dataset.lang === selected);
  });
}

function applyLanguage(lang) {
  const selected = translations[lang] ? lang : 'en';
  const page = document.body.dataset.page;
  const dict = translations[selected];

  document.documentElement.lang = selected;

  if (dict.titles[page]) {
    document.title = dict.titles[page];
  }

  const metaDescription = document.getElementById('meta-description');
  if (metaDescription && dict.meta[page]) {
    metaDescription.setAttribute('content', dict.meta[page]);
  }

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.getAttribute('data-i18n');
    const text = getValueByPath(dict, key);
    if (text !== null) {
      if (node.hasAttribute('data-i18n-html')) {
        node.innerHTML = text;
      } else {
        node.textContent = text;
      }
    }
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((node) => {
    const key = node.getAttribute('data-i18n-aria-label');
    const text = getValueByPath(dict, key);
    if (text !== null) {
      node.setAttribute('aria-label', text);
    }
  });

  const nav = document.querySelector('.nav-links');
  if (nav && dict.nav && dict.nav.home) {
    nav.setAttribute('aria-label', selected === 'fr' ? 'Navigation principale' : selected === 'de' ? 'Hauptnavigation' : selected === 'nl' ? 'Hoofdnavigatie' : 'Main navigation');
  }

  updateLanguageUi(selected);

  localStorage.setItem('siteLanguage', selected);
}

function initLanguageSwitcher() {
  const toggle = document.getElementById('lang-toggle');
  const menu = document.getElementById('lang-menu');
  const options = Array.from(document.querySelectorAll('.lang-option'));

  if (!toggle || !menu || options.length === 0) {
    return;
  }

  const saved = localStorage.getItem('siteLanguage');
  const browser = (navigator.language || 'en').slice(0, 2).toLowerCase();
  const initial = translations[saved] ? saved : translations[browser] ? browser : 'en';

  applyLanguage(initial);

  toggle.addEventListener('click', () => {
    const isOpen = menu.classList.toggle('open');
    toggle.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
  });

  options.forEach((option) => {
    option.addEventListener('click', () => {
      const selected = option.dataset.lang;
      applyLanguage(selected);
      menu.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    });
  });

  document.addEventListener('click', (event) => {
    if (!menu.contains(event.target) && !toggle.contains(event.target)) {
      menu.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    }
  });
}

function initCasesCarousel() {
  const carousel = document.getElementById('cases-carousel');
  const prev = document.getElementById('cases-prev');
  const next = document.getElementById('cases-next');

  if (!carousel || !prev || !next) {
    return;
  }

  const getStep = () => {
    const first = carousel.querySelector('.case-slide');
    if (!first) {
      return 320;
    }
    const gap = 12;
    return first.getBoundingClientRect().width + gap;
  };

  prev.addEventListener('click', () => {
    carousel.scrollBy({ left: -getStep(), behavior: 'smooth' });
  });

  next.addEventListener('click', () => {
    carousel.scrollBy({ left: getStep(), behavior: 'smooth' });
  });

  carousel.querySelectorAll('.case-slide').forEach((slide) => {
    slide.addEventListener('click', () => {
      const park = slide.dataset.park;
      if (!park) {
        return;
      }
      window.location.href = `videos.html?park=${encodeURIComponent(park)}`;
    });
  });
}

function formatCompactViews(value) {
  const n = Number(value || 0);
  if (n >= 1000000) {
    return `${(n / 1000000).toFixed(1).replace(/\.0$/, '')}M`;
  }
  if (n >= 1000) {
    return `${(n / 1000).toFixed(1).replace(/\.0$/, '')}K`;
  }
  return `${n}`;
}

async function initCaseStatsFromJson() {
  const allStatsFiles = ['stats_compact.json', 'stats.json'];
  const statsBaseCandidates = ['stats/', './stats/', '../stats/', 'MediaKit/stats/', '/MediaKit/stats/', '/stats/'];

  const parkHashtagsMap = {
    'europapark': ['#europapark'],
    'efteling': ['#efteling'],
    'bobbejaanland': ['#bobbejaanland', '#bobejaanland'],
    'bobejaanland': ['#bobbejaanland', '#bobejaanland'],
    'toverland': ['#toverland'],
    'phantasialand': ['#phantasialand'],
    'nigloland': ['#nigloland'],
    'rulantica': ['#rulantica'],
    'energylandia': ['#energylandia'],
    'parc-asterix': ['#parcasterix', '#parc-asterix'],
    'traumatica': ['#traumatica'],
    'seaworld': ['#seaworld', '#seaworldorlando'],
    'universal': ['#universalorlando', '#epicuniverse', '#islandsofadventure', '#islandofadventure', '#universalstudiosorlando'],
    'disneyworld': ['#disneyworld', '#waltdisneyworld']
  };

  const parkKeywordsMap = {
    'europapark': ['europa-park', 'europapark'],
    'efteling': ['efteling'],
    'bobbejaanland': ['bobbejaanland', 'bobejaanland'],
    'bobejaanland': ['bobbejaanland', 'bobejaanland'],
    'toverland': ['toverland'],
    'phantasialand': ['phantasialand'],
    'nigloland': ['nigloland'],
    'rulantica': ['rulantica'],
    'energylandia': ['energylandia'],
    'parc-asterix': ['parcasterix', 'parc-asterix', 'parc asterix'],
    'traumatica': ['traumatica'],
    'seaworld': ['seaworld', 'sea world', 'seaworld orlando'],
    'universal': ['universal orlando', 'universalorlando', 'epic universe', 'epicuniverse', 'islands of adventure', 'islandsofadventure'],
    'disneyworld': ['disney world', 'disneyworld', 'walt disney world', 'magic kingdom']
  };

  const forcedVideoUrlsByPark = {
    'seaworld': ['https://www.tiktok.com/@clip2ep.fan/video/7616407866504711446?is_from_webapp=1&sender_device=pc&web_id=7499489214192043542'],
    'universal': ['https://www.tiktok.com/@clip2ep.fan/video/7616407866504711446?is_from_webapp=1&sender_device=pc&web_id=7499489214192043542'],
    'disneyworld': ['https://www.tiktok.com/@clip2ep.fan/video/7616407866504711446?is_from_webapp=1&sender_device=pc&web_id=7499489214192043542']
  };

  const normalizeTikTokUrl = (url) => (url || '').split('?')[0];

  const extractHashtags = (...texts) => {
    const merged = texts.filter(Boolean).join(' ');
    return merged.match(/#[^\s#]+/g) || [];
  };

  const normalizeVideo = (video) => ({
    url: video.url || '',
    views: Number(video.views || 0),
    hashtags: extractHashtags(video.title || '', video.description || '').map((tag) => tag.toLowerCase())
  });

  const extractVideosFromRaw = (raw) => {
    const out = [];

    if (Array.isArray(raw?.videos)) {
      out.push(...raw.videos);
    }

    if (raw?.sources?.tiktok?.videos) {
      for (const item of raw.sources.tiktok.videos) {
        const flat = item.entry_flat || item;
        const full = item.full_info || {};
        out.push({
          url: item.video_url || flat.url || '',
          views: flat.view_count || 0,
          title: flat.title || flat.description || '',
          description: flat.description || full.description || ''
        });
      }
    }

    if (raw?.sources?.youtube?.videos) {
      for (const item of raw.sources.youtube.videos) {
        const flat = item.entry_flat || item;
        const full = item.full_info || {};
        out.push({
          url: item.video_url || flat.url || '',
          views: flat.view_count || flat.views || 0,
          title: flat.title || full.title || flat.description || '',
          description: flat.description || full.description || ''
        });
      }
    }

    if (raw?.plateformes?.tiktok?.videos) {
      out.push(...raw.plateformes.tiktok.videos);
    }

    if (raw?.plateformes?.youtube?.videos) {
      out.push(...raw.plateformes.youtube.videos);
    }

    return out;
  };

  const slides = Array.from(document.querySelectorAll('.case-slide[data-park]'));
  if (slides.length === 0) {
    return;
  }

  const fetchStatsByFileName = async (fileName) => {
    for (const base of statsBaseCandidates) {
      try {
        const response = await fetch(`${base}${fileName}`, { cache: 'no-store' });
        if (!response.ok) {
          continue;
        }
        return await response.json();
      } catch (_) {
        // Try next candidate base path.
      }
    }

    return null;
  };

  let selectedRaw = null;
  for (const fileName of allStatsFiles) {
    selectedRaw = await fetchStatsByFileName(fileName);
    if (selectedRaw) {
      break;
    }
  }

  const rawFiles = selectedRaw ? [selectedRaw] : [];

  const allVideos = rawFiles
    .filter(Boolean)
    .flatMap((raw) => extractVideosFromRaw(raw))
    .map(normalizeVideo);

  slides.forEach((slide) => {
    const park = slide.dataset.park;
    const expected = (parkHashtagsMap[park] || []).map((tag) => tag.toLowerCase());
    const keywords = (parkKeywordsMap[park] || []).map((k) => k.toLowerCase());
    if (expected.length === 0 && keywords.length === 0) {
      return;
    }

    const byUrl = new Map();
    for (const video of allVideos) {
      const byHashtag = expected.length > 0 && video.hashtags.some((tag) => expected.includes(tag));
      const text = `${video.title || ''} ${video.description || ''}`.toLowerCase();
      const byKeyword = keywords.length > 0 && keywords.some((k) => text.includes(k));
      if (!byHashtag && !byKeyword) {
        continue;
      }

      const key = video.url || '';
      const existing = byUrl.get(key);
      if (!existing || video.views > existing.views) {
        byUrl.set(key, video);
      }
    }

    const forcedUrls = forcedVideoUrlsByPark[park] || [];
    for (const forcedUrl of forcedUrls) {
      const forcedNorm = normalizeTikTokUrl(forcedUrl);
      const exists = [...byUrl.values()].some((v) => normalizeTikTokUrl(v.url) === forcedNorm);
      if (exists) {
        continue;
      }
      const fromJson = allVideos.find((v) => normalizeTikTokUrl(v.url) === forcedNorm);
      if (fromJson) {
        byUrl.set(`forced:${forcedNorm}`, fromJson);
      }
    }

    const totalViews = [...byUrl.values()].reduce((sum, v) => sum + v.views, 0);

    const label = slide.querySelector('.case-overlay p span');
    const row = slide.querySelector('.case-overlay p');
    if (!row) {
      return;
    }

    const suffixKey = label?.getAttribute('data-i18n') || 'cases.viewsLabel';
    const currentLang = localStorage.getItem('siteLanguage') || 'fr';
    const currentDict = translations[currentLang] || translations.en;
    const suffixText = getValueByPath(currentDict, suffixKey) || (label ? label.textContent : 'total views');
    row.innerHTML = `${formatCompactViews(totalViews)} <span data-i18n="${suffixKey}">${suffixText}</span>`;
  });
}

function initHeroBackgroundVideo() {
  const video = document.getElementById('hero-video');
  if (!video) {
    return;
  }

  video.muted = true;
  video.defaultMuted = true;
  video.playsInline = true;

  const tryPlay = () => {
    const playAttempt = video.play();
    if (playAttempt && typeof playAttempt.catch === 'function') {
      playAttempt.catch(() => {
        // Browser autoplay policies may still block playback until interaction.
      });
    }
  };

  if (video.readyState >= 2) {
    tryPlay();
  } else {
    video.addEventListener('loadeddata', tryPlay, { once: true });
  }

  video.addEventListener('canplay', tryPlay);

  document.addEventListener('visibilitychange', () => {
    if (!document.hidden) {
      tryPlay();
    }
  });
}

